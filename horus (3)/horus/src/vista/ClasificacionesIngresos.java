    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:                                           
        String tipoClasificacion = txtNCIngreso.getText().trim();

        if (tipoClasificacion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El tipo de clasificación no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }

        if (isTipoClasificacionExists(tipoClasificacion)) {
            JOptionPane.showMessageDialog(this, "El tipo de clasificación ya existe en la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }



        cCI.insertarClasificacionIngresos(txtNCIngreso);
        ctrl.limpiar(txtId, txtNCIngreso);
        cCI.mostrarClasificacionIngresos(tbTotalClasificaciones);
        
    }//GEN-LAST:event_btnGuardarActionPerformed

