    private boolean isTipoClasificacionExists(String tipoClasificacion) {
        DBConexion dbConexion = new DBConexion();
        Connection connection = null;
        PreparedStatement preparedStatement = null;
        ResultSet resultSet = null;
        boolean exists = false;

        try {
            connection = dbConexion.establecerConexion();

            String query = "SELECT COUNT(*) FROM clasificaciones_ingresos WHERE nombreClasificacionIngreso = ?";
            preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, tipoClasificacion);
            resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                exists = resultSet.getInt(1) > 0;
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Ocurrió un error al verificar el tipo de clasificación.", "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (resultSet != null) resultSet.close();
                if (preparedStatement != null) preparedStatement.close();
                if (connection != null) connection.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        return exists;
    }


