    private String validarCodigoFuente(String codigoFuente) {
        if (codigoFuente.length() != 8) {
            return "El código de fuente debe tener exactamente 8 caracteres.";
        }
        return null; 
    }
    private String validarNombreFuente(String nombreFuente) {
        if (nombreFuente.isEmpty()) {
            return "El nombre de la fuente no puede estar vacía.";
        }
        return null; 
    }
    private String validarOrigenFuente(String origenFuente) {
        if (origenFuente.isEmpty()) {
            return "El origen de la fuente no puede estar vacía.";
        }
        return null; 
    }
    
    private void mostrarErrores(StringBuilder errores) {
        if (errores.length() > 0) {
            JOptionPane.showMessageDialog(this, errores.toString(), "Errores de Validación", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    
     private boolean validarCampos() {
        String codigoFuente = txtCodigoFuente.getText().trim();
        String nombreFuente = txtNombreFuente.getText().trim();
        String Origen = txtOrigenFuente.getText().trim();
        
        StringBuilder errores = new StringBuilder();

        String errorCodigo = validarCodigoFuente(codigoFuente);
        if (errorCodigo != null) {
            errores.append(errorCodigo).append("\n");
        }
        
        String errorNombre = validarNombreFuente(nombreFuente);
        if (errorNombre != null) {
            errores.append(errorNombre).append("\n");
        }
        
        String errorOrigen = validarOrigenFuente(Origen);
        if (errorOrigen != null) {
            errores.append(errorOrigen).append("\n");
        }
        
        mostrarErrores(errores);

        return errores.length() == 0;
    }
    
    
